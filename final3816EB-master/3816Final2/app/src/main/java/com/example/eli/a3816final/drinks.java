package com.example.eli.a3816final;

public class drinks
{
    String id;
    float rating;

    public drinks()
    {

    }
    public drinks(String id, float rating)
    {
        this.id = id;
        this.rating = rating;
    }
    public float getDrinks()
    {
        return rating;
    }
    public String getId()
    {
        return id;
    }

}
